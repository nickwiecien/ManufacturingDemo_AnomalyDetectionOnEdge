import asyncio
import sys
import signal
import threading
from azure.iot.device.aio import IoTHubModuleClient
import json
import requests

# Event indicating client stop
stop_event = threading.Event()


def create_client():
    client = IoTHubModuleClient.create_from_edge_environment()

    # Define function for handling received messages
    async def receive_message_handler(message):

        if message.input_name == "rawtelemetry":
            raw_data = json.loads(message.data.decode('utf-8'))
            timestamp  = raw_data['timestamp']
            del raw_data['timestamp']
            del raw_data['machine_status']
            raw_data = [raw_data]
            print(raw_data)
            headers = {'Content-Type': 'application/json'}
            response =  requests.post('http://AnomalyDetection:5001/score', data=json.dumps(raw_data), headers=headers)
            resp_obj =  response.json()
            anomaly_detected =  resp_obj[0]
            if anomaly_detected:
                print(f'MACHINE STATE: ANOMALOUS CONDITIONS DETECTED - {timestamp}')
            else:
                print(f'MACHINE STATE: OK - {timestamp}')
            print()
            # await client.send_message_to_output(message, "output1")

    try:
        # Set handler on the client
        client.on_message_received = receive_message_handler
    except:
        # Cleanup if failure occurs
        client.shutdown()
        raise

    return client


async def run_sample(client):
    # Customize this coroutine to do whatever tasks the module initiates
    # e.g. sending messages
    while True:
        await asyncio.sleep(1000)


def main():
    if not sys.version >= "3.5.3":
        raise Exception( "The sample requires python 3.5.3+. Current version of Python: %s" % sys.version )
    print ( "IoT Hub Client for Python" )

    # NOTE: Client is implicitly connected due to the handler being set on it
    client = create_client()

    # Define a handler to cleanup when module is is terminated by Edge
    def module_termination_handler(signal, frame):
        print ("IoTHubClient sample stopped by Edge")
        stop_event.set()

    # Set the Edge termination handler
    signal.signal(signal.SIGTERM, module_termination_handler)

    # Run the sample
    loop = asyncio.get_event_loop()
    try:
        loop.run_until_complete(run_sample(client))
    except Exception as e:
        print("Unexpected error %s " % e)
        raise
    finally:
        print("Shutting down IoT Hub Client...")
        loop.run_until_complete(client.shutdown())
        loop.close()


if __name__ == "__main__":
    main()
